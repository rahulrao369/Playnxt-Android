package com.cw.playnxt.model.StaticModel;

public class YourStatsModel {
    String h1;
    String h2;
    String h3;
    int number1;
    int num1;

    public YourStatsModel(String h1, String h2, String h3, int number1, int num1) {
        this.h1 = h1;
        this.h2 = h2;
        this.h3 = h3;
        this.number1 = number1;
        this.num1 = num1;
    }

    public String getH1() {
        return h1;
    }

    public void setH1(String h1) {
        this.h1 = h1;
    }

    public String getH2() {
        return h2;
    }

    public void setH2(String h2) {
        this.h2 = h2;
    }

    public String getH3() {
        return h3;
    }

    public void setH3(String h3) {
        this.h3 = h3;
    }

    public int getNumber1() {
        return number1;
    }

    public void setNumber1(int number1) {
        this.number1 = number1;
    }

    public int getNum1() {
        return num1;
    }

    public void setNum1(int num1) {
        this.num1 = num1;
    }
}
